%% ====================================================================
%% Internal functions
%% ====================================================================

%% 生成玩家昵称
make_player_name() ->
	case util:rand(1, 2) of
		1 ->
			PrefixList = ?PLAYER_PREFIX_LIST_1,
            InfixList = ?PLAYER_INFIX_LIST_1;
		_ ->
			PrefixList = ?PLAYER_PREFIX_LIST_2,
            InfixList = ?PLAYER_INFIX_LIST_2
	end,
	SuffixList = ?PLAYER_SUFFIX_LIST,
	Prefix = lists:nth(util:rand(1, length(PrefixList)), PrefixList),
	Infix = lists:nth(util:rand(1, length(InfixList)), InfixList),
	Suffix = lists:nth(util:rand(1, length(SuffixList)), SuffixList),
	lists:concat([Prefix, Infix, Suffix]).

%% 生成竞技场机器人昵称
make_pvp_name() ->
	PrefixList = ?PVP_PREFIX_LIST,
	SuffixList = ?PVP_SUFFIX_LIST,
	Prefix = lists:nth(util:rand(1, length(PrefixList)), PrefixList),
	Suffix = lists:nth(util:rand(1, length(SuffixList)), SuffixList),
	%io:format("make_pvp_name___1 ~ts~n", [erlang:list_to_binary(lists:concat([Prefix, Suffix]))]),
	lists:concat([Prefix, Suffix]).


